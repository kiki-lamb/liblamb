    inline size_t generate_phase_increment(
      size_t clock_hz,
      double freq,
      size_t max_phase = SIZE_MAX
    ) {
      size_t tmp = lroundf(max_phase/clock_hz*((double)freq));
      //printf("%lf => %u\n", freq, tmp);
      return tmp;
    }
    
    inline void generate_phase_increments(
      size_t clock_hz,
      size_t * phase_incrs_buffer, // output
      double const * freqs_buffer, // input
      size_t tsize
    ) {
      for (size_t ix = 0; ix < tsize; ix++) {
        //printf("%d ", ix);
        phase_incrs_buffer[ix] = generate_phase_increment(clock_hz, freqs_buffer[ix]);
      }
    }
